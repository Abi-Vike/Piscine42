int ft_strncmp(char *s1, char *s2, unsigned int n)
{
    unsigned	int i;
	
	i = 0;
    while (*s1 && *s2 && *s1 == *s2 && i < n - 1)
	{
		s1++;
		s2++;
		i++;
	}
	if ((i == n - 1 || n == 0) || (*s1 == '\0' && *s2 == '\0'))
        return 0;
    else
        return *s1 - *s2;
}
